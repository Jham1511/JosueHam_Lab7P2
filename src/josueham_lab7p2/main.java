    public DefaultListModel llenarListaArch(adminArchivos a) {
        DefaultListModel modelo = new DefaultListModel<>();
        for (Archivo ar : a.getListaArchivos()) {
            modelo.addElement(ar.toString());
        }
        return modelo;
    }

    public void escribirBit(adminArchivos a) throws IOException {
        FileWriter fw = null;
        BufferedWriter bw = null;
        try {
            fw = new FileWriter(archivo, true);
            bw = new BufferedWriter(fw);
            for (Archivo t : a.getListaArchivos()) {
                bw.write(t.getNombre() + "->");
                bw.write(t.getLink() + "->");
                bw.write(new Date().toInstant().toString() + "\n");
            }
            bw.flush();
        } catch (Exception ex) {
        }
        bw.close();
        fw.close();
    }

